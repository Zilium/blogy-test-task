<?php

use Core\Container;

/**
 * -----------------------------------------------------------------------------
 * Профиль инициализации сервисов: full.
 * -----------------------------------------------------------------------------
 * 
 * Загружает полный набор сервисов приложения, включая
 * конфигурацию, работу сессий, базу данных, HTTP-компоненты,
 * слой представлений и обработку ошибок.
 *
 * Используется при стандартном запуске приложения
 * через {@see App\Http\AppKernel}, обеспечивая полное окружение ядра.
 * 
 * -----------------------------------------------------------------------------
 * Архитектурные особенности:
 * -----------------------------------------------------------------------------
 * 
 * • Профиль определяет полный порядок инициализации зависимостей;
 * • Каждый подконфиг может опираться на ранее загруженные сервисы;
 * • Отсутствующие подконфиги пропускаются без ошибок;
 * • Может расширяться и кастомизироваться при необходимости
 *   (например, добавление сервисов логирования, кэша и т.д.).
 * 
 * @package config\services\profiles
 *
 * @param Container $container Контейнер внедрения зависимостей.
 * 
 * @return void
 */
return function (Container $container): void {
    /**
     * Последовательность подключения подконфигов.
     *
     * Определяет порядок инициализации всех ключевых сервисов приложения,
     * где каждый последующий может зависеть от предыдущего.
     */
    $order = [
        'config.php',
        'session.php',
        'db.php',
        'http.php',
        'view.php',
        'error.php',
    ];

    foreach ($order as $file) {
        $path = SERVICES_DIR . '/' . $file;

        if (!file_exists($path)) {
            continue;
        }

        $register = require $path;
        
        if (is_callable($register)) {
            $register($container);
        }
    }
};