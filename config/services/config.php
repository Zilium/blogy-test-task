<?php

use Core\Container;
use Core\Services\Config;

/**
 * -----------------------------------------------------------------------------
 * Регистрация сервиса конфигурации приложения.
 * -----------------------------------------------------------------------------
 * Определяет singleton-экземпляр {@see Core\Services\Config} в DI-контейнере.
 * Сервис используется для централизованного доступа к параметрам конфигурации.
 *
 * ---------------------------------------------------------------------------
 * Пример использования:
 * ---------------------------------------------------------------------------
 * ```php
 * use Core\Services\Config;
 *
 * $config = $container->get(Config::class);
 * $version = $config->get('version');
 * ```
 * 
 * @package config\services
 * 
 * @param Container $container Контейнер внедрения зависимостей.
 *
 * @return void
 */
return function (Container $container): void {
    /**
     * ------------------------------------------------------------
     * Добавление singleton-сервиса Config.
     * ------------------------------------------------------------
     * Обеспечивает единый экземпляр {@see Config} во всём приложении.
     */
    $container->singleton(Config::class, new Config());
};