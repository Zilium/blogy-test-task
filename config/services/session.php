<?php

use Core\Container;
use Core\Services\Session;

/**
 * -----------------------------------------------------------------------------
 * Регистрация сервиса управления сессиями.
 * -----------------------------------------------------------------------------
 * Отвечает за инициализацию и управление пользовательскими сессиями приложения.
 * 
 * Объект {@see Core\Services\Session} реализует шаблон Singleton,
 * обеспечивая единый экземпляр сессии на протяжении всего жизненного цикла запроса.
 *
 * -----------------------------------------------------------------------------
 * Архитектурные особенности:
 * -----------------------------------------------------------------------------
 * • Сессия создаётся лениво - при первом обращении к {@see Session::getInstance()};
 * • Класс {@see Session} инкапсулирует нативный механизм PHP-сессий,
 *   предоставляя объектно-ориентированный интерфейс;
 * • Управление запуском и завершением сессии выполняется ядром
 *   ({@see Core\Kernel::startSession()}).
 *
 * -----------------------------------------------------------------------------
 * Пример использования:
 * -----------------------------------------------------------------------------
 * ```php
 * use Core\Services\Session;
 *
 * $session = $container->get(Session::class);
 * $session->set('user_id', 123);
 * $userId = $session->get('user_id');
 * ```
 *
 * @package config\services
 *
 * @param Container $container Контейнер внедрения зависимостей.
 *
 * @return void
 */
return function (Container $container): void {
    /**
     * ------------------------------------------------------------
     * Session: управление сессиями.
     * ------------------------------------------------------------
     * Сервис регистрируется с использованием ленивой инициализации:
     * экземпляр {@see Core\Services\Session} создаётся только при первом вызове.
     * 
     * Метод {@see Session::getInstance()} использует трейд {@see Core\Traits\TSingleton},
     * обеспечивая единый объект соединения с сессией в рамках
     * жизненного цикла приложения и возможность статического обращения:
     * `Session::getInstance()->get('user_id');`.
     */
    $container->singleton(Session::class, fn() => Session::getInstance());
};