<?php
declare(strict_types=1);

namespace Core\Routing;

use RuntimeException;

/**
 * -----------------------------------------------------------------------------
 * Класс RouteLoader
 * -----------------------------------------------------------------------------
 * Отвечает за загрузку маршрутов приложения для конкретной зоны (`web`, `admin`, `api`).
 *
 * Не зависит от физического расположения файлов: пути к исходникам и кэшу
 * передаются извне. Используется ядром для кэшируемой регистрации маршрутов.
 *
 * -----------------------------------------------------------------------------
 * ОСНОВНЫЕ ФУНКЦИИ:
 * -----------------------------------------------------------------------------
 *  • Проверяет наличие и актуальность файла маршрутов;
 *  • При необходимости пересобирает кэш маршрутов;
 *  • Загружает маршруты из исходного файла или кэша;
 *  • Передаёт данные в {@see Router} для дальнейшей работы.
 *
 * -----------------------------------------------------------------------------
 * СТРУКТУРА:
 * -----------------------------------------------------------------------------
 *  ├─ /config/routes/{zone}.php — исходные файлы маршрутов
 *  └─ /storage/cache/routes_{zone}.php — кэш маршрутов
 *
 * -----------------------------------------------------------------------------
 * ПРИМЕР:
 * -----------------------------------------------------------------------------
 * ```php
 * RouteLoader::load('admin', config_path('routes'), storage_path('cache/routes'));
 * ```
 * 
 * @package Core\Routing
 */
final class RouteLoader
{
    /**
     * Загружает маршруты для указанной зоны.
     *
     * Проверяет наличие и актуальность кэша; если он устарел — пересобирает.
     * В противном случае восстанавливает маршруты напрямую из кэша.
     *
     * @param string $zone Имя зоны маршрутов (например, `web` или `admin`).
     * @param string $sourceDir Путь к директории исходных файлов маршрутов.
     * @param string $cacheDir Путь к директории файлов кэша маршрутов.
     *
     * @throws RuntimeException Если исходный файл маршрутов не найден.
     * @return void
     */
    public static function load(string $zone, string $sourceDir, string $cacheDir): void
    {
        /**
         * ------------------------------------------------------------
         * Формируем пути к исходному файлу и файлу кэша
         * ------------------------------------------------------------
         */
        $sourceFile = "{$sourceDir}/{$zone}.php";
        $cacheFile  = "{$cacheDir}/routes_{$zone}.php";

        /**
         * ------------------------------------------------------------
         * Проверяем наличие исходного файла маршрутов
         * ------------------------------------------------------------
         */
        if (!file_exists($sourceFile)) {
            throw new RuntimeException("Файл маршрутов для зоны '{$zone}' не найден: {$sourceFile}");
        }

        /**
         * ------------------------------------------------------------
         * Готовим директорию для кэша
         * ------------------------------------------------------------
         */
        if (!is_dir($cacheDir)) {
            mkdir($cacheDir, 0755, true);
        }

        /**
         * ------------------------------------------------------------
         * Если кэш актуален — восстанавливаем маршруты напрямую.
         * ------------------------------------------------------------
         */
        if (self::isCacheValid($cacheFile, $sourceFile)) {
            $cachedRoutes = require $cacheFile;
            Router::setRoutes($cachedRoutes);
            return;
        }

        /**
         * ------------------------------------------------------------
         * Подключаем оригинальный файл маршрутов
         * ------------------------------------------------------------
         * Он должен зарегистрировать маршруты в {@see Router}.
         */
        require $sourceFile;

        /**
         * ------------------------------------------------------------
         * Получаем все зарегистрированные маршруты и создаём кэш
         * ------------------------------------------------------------
         */
        $routes = Router::getRoutes();
        self::writeCache($cacheFile, $routes);
    }

    /**
     * Проверяет, актуален ли кэш относительно исходного файла маршрутов.
     *
     * @param string $cacheFile Путь к файлу кэша.
     * @param string $sourceFile Путь к исходному файлу маршрутов.
     * 
     * @return bool True, если кэш существует и свежее исходного файла.
     */
    private static function isCacheValid(string $cacheFile, string $sourceFile): bool
    {
        return file_exists($cacheFile)
            && filemtime($cacheFile) >= filemtime($sourceFile);
    }

    /**
     * Записывает маршруты в кэш.
     *
     * @param string $cacheFile Путь к файлу кэша.
     * @param array $routes Ассоциативный массив маршрутов.
     * 
     * @return void
     */
    private static function writeCache(string $cacheFile, array $routes): void
    {
        $export = var_export($routes, true);
        file_put_contents($cacheFile, "<?php\nreturn {$export};\n");
    }
}