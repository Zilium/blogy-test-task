<?php

declare(strict_types=1);

namespace Core\Contracts;

/**
 * -----------------------------------------------------------------------------
 * Контракт для резолвера контроллеров.
 * -----------------------------------------------------------------------------
 * 
 * Определяет обязательный метод для классов, которые отвечают
 * за разрешение (определение) имени контроллера на основе данных маршрута.
 * 
 * Используется ядром фреймворка для динамического определения,
 * какой контроллер должен быть вызван при обработке HTTP-запроса.
 * 
 * -----------------------------------------------------------------------------
 * Пример:
 * -----------------------------------------------------------------------------
 * Для маршрута:
 * ```php
 * ['controller' => 'home', 'action' => 'index']
 * ```
 * реализация может вернуть класс:
 * ```php
 * App\Http\Controllers\Home
 * ```
 * 
 * -----------------------------------------------------------------------------
 * Особенности реализации:
 * -----------------------------------------------------------------------------
 * • Если маршрут не передан явно, используется текущий маршрут,
 *   получаемый через глобальную функцию {@see route()};
 * • Метод возвращает полное имя класса контроллера (FQCN),
 *   включая пространство имён.
 * 
 * @package Core\Contracts
 */
interface ControllerResolverInterface
{
    /**
     * Разрешает и возвращает полное имя класса контроллера по данным маршрута.
     * 
     * @param array|null $route Ассоциативный массив параметров маршрута
     *                          (например: controller, action, prefix и т.п.).
     *                          Если не указан, используется текущий маршрут.
     * 
     * @return string Полное имя класса контроллера (FQCN).
     */
    public function resolve(?array $route = null): string;
}