<?php
declare(strict_types=1);

namespace App\Foundation\Routing;

/**
 * -----------------------------------------------------------------------------
 * Класс ZoneDetector.
 * -----------------------------------------------------------------------------
 * Определяет текущую зону маршрутизации приложения: `admin`, `public`.
 * 
 * Используется при инициализации маршрутов для выбора нужного набора
 * конфигурационных файлов (`/config/routes/{zone}.php`).
 *
 * -----------------------------------------------------------------------------
 * ЛОГИКА РАБОТЫ:
 * -----------------------------------------------------------------------------
 *  1. Получает URI запроса (переданный явно или извлечённый из окружения);
 *  2. Нормализует путь, исключая query-параметры;
 *  3. Проверяет, начинается ли путь с `/admin` (регулярное выражение);
 *  4. Возвращает одну из зон:
 *     • `admin`  - если URL начинается с `/admin`
 *     • `public` - во всех остальных случаях
 *
 * -----------------------------------------------------------------------------
 * ПРИМЕР:
 * -----------------------------------------------------------------------------
 * ```php
 * $zone = ZoneDetector::detect('/');                     // public
 * $zone = ZoneDetector::detect('/admin');                // admin
 * $zone = ZoneDetector::detect('/admin/settings/users'); // admin
 * ```
 *
 * -----------------------------------------------------------------------------
 * 
 * @package App\Foundation\Routing
 */
final class ZoneDetector
{
    /**
     * Определяет активную зону приложения на основе URI.
     *
     * @param string $uri Полный или относительный URI запроса.
     *                    Пример: `/admin/settings/users`, `/loans/apply`.
     *
     * @return string Возвращает имя зоны маршрутизации (`admin` или `public`).
     */
    public static function detect(string $uri): string
    {
        /**
         * ------------------------------------------------------------
         * Нормализуем путь
         * ------------------------------------------------------------
         * Извлекаем только путь без query-части и параметров.
         */
        $uri = parse_url($uri, PHP_URL_PATH) ?: '/';

        /**
         * ------------------------------------------------------------
         * Проверяем принадлежность к административной зоне
         * ------------------------------------------------------------
         * Совпадение происходит, если путь начинается с `/admin` или `/admin/`.
         */
        $match = preg_match('#^/admin(/|$)#', $uri);

        /**
         * ------------------------------------------------------------
         * Возвращаем определённую зону
         * ------------------------------------------------------------
         * В будущем здесь можно расширить список (например, добавить `api`).
         */
        return match (true) {
            (bool) $match => 'admin',
            default => 'web',
        };
    }
}