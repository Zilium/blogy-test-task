<?php
declare(strict_types=1);

namespace App\Foundation\Providers;

use Core\Routing\RouteLoader;
use App\Foundation\Routing\ZoneDetector;

/**
 * -----------------------------------------------------------------------------
 * Провайдер маршрутов приложения.
 * -----------------------------------------------------------------------------
 * Отвечает за определение текущей зоны (Public, Admin),
 * а также за загрузку соответствующих маршрутов через {@see RouteLoader}.
 *
 * Используется ядром при инициализации приложения для динамической загрузки
 * маршрутов в зависимости от контекста HTTP-запроса.
 *
 * -----------------------------------------------------------------------------
 * ОСНОВНЫЕ ЭТАПЫ РАБОТЫ:
 * -----------------------------------------------------------------------------
 *  1. Определяет текущую зону через {@see ZoneDetector};
 *  2. Указывает путь к файлам маршрутов и кэшу;
 *  3. Передаёт управление {@see RouteLoader}, который:
 *     • Загружает маршруты из `config/routes/{zone}.php`;
 *     • Кэширует собранную таблицу маршрутов;
 *     • Регистрирует их в системе.
 *
 * -----------------------------------------------------------------------------
 * ПРИМЕР ИСПОЛЬЗОВАНИЯ:
 * -----------------------------------------------------------------------------
 * ```php
 * // Внутри WebKernel:
 * $uri = $request->getRequestUri();
 * RouteServiceProvider::register($uri);
 * ```
 *
 * В результате:
 *  - Для /admin/... будет загружен config/routes/admin.php
 *  - Для /... будет загружен config/routes/public.php
 *
 * -----------------------------------------------------------------------------
 * 
 * @package App\Foundation\Providers
 */
final class RouteServiceProvider
{
    /**
     * Регистрирует маршруты приложения.
     *
     * Определяет активную зону (например, Admin или Public),
     * находит соответствующий файл маршрутов и делегирует
     * их загрузку и кэширование {@see RouteLoader}.
     *
     * @param string $uri Текущий URI запроса (например, "/admin/settings/users" или "/loans/apply").
     *
     * @return void
     */
    public static function register(string $uri): void
    {
        /**
         * ------------------------------------------------------------
         * Определяем текущую зону приложения.
         * ------------------------------------------------------------
         * ZoneDetector анализирует URI и возвращает контекстную зону,
         * например: "admin", или "public".
         */
        $zone = ZoneDetector::detect($uri);
  
        /**
         * ------------------------------------------------------------
         * Указываем пути до исходных маршрутов и кэша.
         * ------------------------------------------------------------
         * Все файлы маршрутов располагаются в routes/{zone}.php
         * После загрузки RouteLoader сохраняет кэш в /storage/cache/routes/.
         */
        $sourceDir = routes_path();

        $cacheDir = storage_path('cache/routes');

        /**
         * ------------------------------------------------------------
         * Загружаем маршруты текущей зоны.
         * ------------------------------------------------------------
         * RouteLoader выполняет:
         *  - чтение исходных файлов маршрутов;
         *  - валидацию структуры;
         *  - формирование кэша;
         *  - регистрацию маршрутов.
         */
        RouteLoader::load($zone, $sourceDir, $cacheDir);
    }
}